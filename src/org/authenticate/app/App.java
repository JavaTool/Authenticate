package org.authenticate.app;

public final class App {
	
	private String appId;
	
	private String appKey;
	
	private String appName;
	
	private String appTable;
	
	private String appEntry;

	public String getAppId() {
		return appId;
	}

	public void setAppId(String appId) {
		this.appId = appId;
	}

	public String getAppKey() {
		return appKey;
	}

	public void setAppKey(String appKey) {
		this.appKey = appKey;
	}

	public String getAppName() {
		return appName;
	}

	public void setAppName(String appName) {
		this.appName = appName;
	}

	public String getAppTable() {
		return appTable;
	}

	public void setAppTable(String appTable) {
		this.appTable = appTable;
	}

	public String getAppEntry() {
		return appEntry;
	}

	public void setAppEntry(String appEntry) {
		this.appEntry = appEntry;
	}

}
